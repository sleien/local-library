import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export function formatDate(value: string | null | undefined): string {
  if (!value) return "";
  const d = new Date(value);
  if (Number.isNaN(d.getTime())) return "";
  return d.toLocaleDateString(undefined, { year: "numeric", month: "short", day: "numeric" });
}

// Physical condition grades for a copy. Keys match the backend; labels are shown
// in the UI. Descriptive only — condition does not affect lending.
export const COPY_CONDITIONS = [
  { value: "new", label: "New" },
  { value: "like_new", label: "Like New" },
  { value: "good", label: "Good" },
  { value: "fair", label: "Fair" },
  { value: "poor", label: "Poor" },
  { value: "damaged", label: "Damaged" },
] as const;

export function conditionLabel(value: string | null | undefined): string {
  if (!value) return "";
  return COPY_CONDITIONS.find((c) => c.value === value)?.label ?? value;
}
